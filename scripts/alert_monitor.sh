#!/bin/bash
# Модул за автоматизирано известяване при критични Syslog събития
# Следи /var/log/syslog за неуспешни опити за вход и реагира с три действия:
# 1. Записва инцидента в отделен файл (critical_alerts.log)
# 2. Симулира изпращане на email до администратора
# 3. Изпраща broadcast (wall) съобщение до всички активни сесии

ALERT_FILE="/var/log/critical_alerts.log"

echo "[*] Модулът за известяване е стартиран. Работи във фонов режим..."

tail -F /var/log/syslog | grep --line-buffered -i "login failure" | while read -r line ; do

echo "$(date): АЛАРМА! Засечен е неоторизиран достъп: $line" >> "$ALERT_FILE"

echo -e "\n====================================================="
echo "🚨 СИСТЕМА ЗА СИГУРНОСТ: Изпращане на email до admin@ict.local..."
echo "ДЕТАЙЛИ: $line"
echo "====================================================="

wall "ВНИМАНИЕ! Засечен е опит за пробив. Проверете $ALERT_FILE"

done
